# // - ціла частина від розподілу 2333 // 1000 = 2
# % - залишок від ділення 3332 % 10 = 2
# ввести з клавіатури 4-х значне число та вивести всі цифри цього числа
# число не має містити 0 в початку
# використовувати % та // для вирішення
number = 5053
n1 = number // 1000
n2 = number // 100 % 10
n3 = number // 10 % 10
n4 = number % 10
print(n1)
print(n2)
print(n3)
print(n4)
